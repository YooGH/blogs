// 欧几里得算法

function aa(a1, a2) {
  let a,b,c;
  a = a1>a2 ? a1 : a2;
  b = a1>a2 ? a2 : a1;
  c = a%b;
  return c===0 ? b : aa(b,c)
}
console.log(aa(1997, 615))
console.log(aa(6, 4))

/* 
  假如需要求 1997 和 615 两个正整数的最大公约数,用欧几里得算法，是这样进行的：
  1997 ÷ 615 = 3 (余 152)
  615 ÷ 152 = 4(余7)
  152 ÷ 7 = 21(余5)
  7 ÷ 5 = 1 (余2)
  5 ÷ 2 = 2 (余1)
  2 ÷ 1 = 2 (余0)
  至此，最大公约数为1
*/